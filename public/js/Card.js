function Card (name, price, stock, brand, category, shortDescription, fullDescription, freeDelivery, fromAge, toAge, aM, image) {
    this.name = name;
    this.price = price;
    this.stock = stock;
    this.brand = brand;
    this.category = category;
    this.shortDescription = shortDescription;
    this.fullDescription = fullDescription;
    this.freeDelivery = freeDelivery;
    this.fromAge = fromAge;
    this.toAge = toAge;
    this.aM = aM;
    this.image = image;
}
